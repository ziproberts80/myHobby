
package printfformating;


public class PrintfFormating {


    public static void main(String[] args) {
        System.out.printf("%-8.5f\n",1.34);
        System.out.printf("%-8.5f\n",1.3400);
        System.out.printf("%-8.5f\n",10.340);
        System.out.printf("%-8.5f\n",10000.34000);
        System.out.printf("%-8.5f\n",10000.340000000);  
    }
    
}
