
package plusplus;


public class PlusPlus {


    public static void main(String[] args) {
       int x = 100;
       
        System.out.println(x);
        System.out.println("The value of x is now " + (++x));
        
        System.out.println(x);
        System.out.println("The value of x is now " + (x++));
        
        System.out.println(x);
        
        
    }
    
}
